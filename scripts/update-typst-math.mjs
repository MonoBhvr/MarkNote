import { mkdir, writeFile } from 'node:fs/promises'
import { dirname } from 'node:path'
import { fileURLToPath } from 'node:url'

const SOURCE_URL = 'https://raw.githubusercontent.com/MonoBhvr/typstMathOnly/main/typst-math.js'
const TARGET_FILE = fileURLToPath(new URL('../lib/marknote/typst-math.mjs', import.meta.url))

function transformToEsm (source) {
  const startMarker = '(function (root) {'
  const strictMarker = '  "use strict";\n\n'
  const exportMarker = '  root.typstMathToLatex = typstMathToLatex;'
  const endMarker = '})(typeof globalThis !== "undefined" ? globalThis : window);'

  if (!source.startsWith(startMarker)) {
    throw new Error('Unexpected Typst math wrapper start')
  }

  const strictIndex = source.indexOf(strictMarker)
  if (strictIndex < 0) {
    throw new Error('Unexpected Typst math strict-mode marker')
  }

  const exportIndex = source.indexOf(exportMarker)
  if (exportIndex < 0) {
    throw new Error('Unexpected Typst math global export marker')
  }

  const endIndex = source.indexOf(endMarker, exportIndex)
  if (endIndex < 0) {
    throw new Error('Unexpected Typst math wrapper end')
  }

  const body = source.slice(strictIndex + strictMarker.length, exportIndex).trimEnd()
  return [
    `// Generated by scripts/update-typst-math.mjs from ${SOURCE_URL}.`,
    '// Do not edit manually.',
    '',
    body,
    '',
    'export { typstMathToLatex }',
    ''
  ].join('\n')
}

const response = await fetch(SOURCE_URL)
if (!response.ok) {
  throw new Error(`Failed to fetch Typst math source: ${response.status} ${response.statusText}`)
}

const source = await response.text()
const output = transformToEsm(source)

await mkdir(dirname(TARGET_FILE), { recursive: true })
await writeFile(TARGET_FILE, output)
console.log(`Updated ${TARGET_FILE} from ${SOURCE_URL}`)
